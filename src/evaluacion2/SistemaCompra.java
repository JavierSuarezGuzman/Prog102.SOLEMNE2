    private void cbTipoProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoProductoActionPerformed
        int posItem = cbTipoProducto.getSelectedIndex();
        if (posItem >= 0) {
            switch (posItem) {
                case 0: //seleccione
                    txtNombre.setText("");
                    txtCodigo.setText("");
                    txtValor.setText("");
                    txtStock.setText("");
                    grupoIVA.clearSelection();
                    txtValorNeto.setText("");
                    txtValorIVA.setText("");
                    txtValorTotal.setText("");
                    break;
                case 1: //periféricos
                    txtNombre.setText("Periféricos");
                    txtCodigo.setText("100");
                    txtValor.setText("20000");
                    txtStock.setText("10");
                    txtStock.setEditable(true);
                    break;
                case 2: //pantalla
                    txtNombre.setText("Pantalla");
                    txtCodigo.setText("200");
                    txtValor.setText("80000");
                    txtStock.setText("15");
                    txtStock.setEditable(true);
                    break;
                case 3: //discos duros
                    txtNombre.setText("Discos duros");
                    txtCodigo.setText("300");
                    txtValor.setText("120000");
                    txtStock.setText("50");
                    txtStock.setEditable(true);
                    break;
                case 4: //otros
                    txtNombre.setText("Otros");
                    txtCodigo.setText("400");
                    txtValor.setText("Ingrese valor");
                    txtValor.setEditable(true);
                    txtStock.setText("Ingrese stock");
                    txtStock.setEditable(true);
                    txtValor.requestFocus();
                    break;
            }
        }
    }//GEN-LAST:event_cbTipoProductoActionPerformed

