    private void txtValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyTyped
        char isNumeric = evt.getKeyChar();
        if (Character.isLetter(isNumeric)) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "El valor debe ser numérica");
        }
    }//GEN-LAST:event_txtValorKeyTyped

    private void txtStockKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStockKeyTyped
        char isNumeric = evt.getKeyChar();
        if (Character.isLetter(isNumeric)) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "El valor debe ser numérica");
        }
    }//GEN-LAST:event_txtStockKeyTyped

    private void rbtSIVAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtSIVAActionPerformed
        int posItem = cbTipoProducto.getSelectedIndex();
        if (posItem > 0) {
            int stock = Integer.parseInt(txtStock.getText());
            int valorNeto = Integer.parseInt(txtValor.getText());//convierto txtValor a int para hacer los calculos 
            int valorIVA, valorTotal; //declaro todo int despues casteo
            valorNeto = (int) valorNeto * stock;
            valorIVA = (int) (valorNeto * 0.19) * stock;
            valorTotal = (int) (valorNeto * 1.19) * stock;
            if (valorNeto > 0 && stock > 0) {
                txtValorNeto.setText(Integer.toString(valorNeto));
                txtValorIVA.setText(Integer.toString(valorIVA));
                txtValorTotal.setText(Integer.toString(valorTotal));
            } else {
                JOptionPane.showMessageDialog(null, "El stock y el valor deben ser mayores que 0");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto");
        }
    }//GEN-LAST:event_rbtSIVAActionPerformed

    private void rbtNOVAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtNOVAActionPerformed
        int posItem = cbTipoProducto.getSelectedIndex();
        if (posItem > 0) {
            int stock = Integer.parseInt(txtStock.getText());
            int valorNeto = Integer.parseInt(txtValor.getText());//convierto txtValor a int para hacer los calculos 
            valorNeto = (int) valorNeto * stock;
            if (valorNeto > 0 && stock > 0) {
                txtValorIVA.setText("");
                txtValorNeto.setText(Integer.toString(valorNeto));
                txtValorTotal.setText(Integer.toString(valorNeto));
            } else {
                JOptionPane.showMessageDialog(null, "El stock y el valor deben ser mayores que 0");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto");
        }
    }//GEN-LAST:event_rbtNOVAActionPerformed

